# Upper: Deixa os caracteres da String todos Maiúsculos
upper = input('Digite uma palavra ou texto: ').upper()
print(upper)

# Title: Deixa somente o primeiro Caractere da String Maiúsculo
title = input('Digite uma palavra: ').title()
print(title)

# Len: Mostra a quantidade de caracteres em uma variavel, independente se for string, int ou float
len = len(input('Digite uma palavra ou número para contar quantas letras ou algarismos: '))
print(len)