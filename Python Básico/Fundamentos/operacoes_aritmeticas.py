# Soma: Utilizamos o sinal "+"
sum = 10 + 2;
print(sum)

# Subtração: Utilizamos o sinal "-"
sub = 10 - 2;
print(sub)

# Multiplicação: Utilizamos o sinal "*"
mult = 10 * 2;
print(mult)

# Divisão: Utilizamos o sinal "/"
div = 10/2
print(div)

# Módulo (O resto de uma divisão): Utilizamos o sinal "%"
mod = 10%2
print(mod)
