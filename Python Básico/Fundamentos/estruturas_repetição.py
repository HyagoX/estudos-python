# Loop While
contador = 1

while contador < 10:
    print(f'Execução número: {contador}')
    contador += 1

# Loop For
for numero in range(5):
    if numero == 0:
        print('Primeira execução')
    elif numero == 1:
        print('Segunda execução')
    elif numero == 2:
        print('Terceira execução')
    elif numero == 3:
        print('Quarta execução')
    else:
        print('Quinta execução')